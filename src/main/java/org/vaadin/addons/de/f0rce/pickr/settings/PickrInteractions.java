package org.vaadin.addons.de.f0rce.pickr.settings;

public class PickrInteractions {

  private boolean hex = false;
  private boolean rgba = false;
  private boolean hsla = false;
  private boolean hsva = false;
  private boolean cmyk = false;
  private boolean input = false;
  private boolean cancel = false;
  private boolean clear = false;
  private boolean save = true;

  /** @return the hex */
  public boolean isHex() {
    return hex;
  }

  /** @param hex the hex to set */
  public void setHex(boolean hex) {
    this.hex = hex;
  }

  /** @return the rgba */
  public boolean isRgba() {
    return rgba;
  }

  /** @param rgba the rgba to set */
  public void setRgba(boolean rgba) {
    this.rgba = rgba;
  }

  /** @return the hsla */
  public boolean isHsla() {
    return hsla;
  }

  /** @param hsla the hsla to set */
  public void setHsla(boolean hsla) {
    this.hsla = hsla;
  }

  /** @return the hsva */
  public boolean isHsva() {
    return hsva;
  }

  /** @param hsva the hsva to set */
  public void setHsva(boolean hsva) {
    this.hsva = hsva;
  }

  /** @return the cmyk */
  public boolean isCmyk() {
    return cmyk;
  }

  /** @param cmyk the cmyk to set */
  public void setCmyk(boolean cmyk) {
    this.cmyk = cmyk;
  }

  /** @return the input */
  public boolean isInput() {
    return input;
  }

  /** @param input the input to set */
  public void setInput(boolean input) {
    this.input = input;
  }

  /** @return the cancel */
  public boolean isCancel() {
    return cancel;
  }

  /** @param cancel the cancel to set */
  public void setCancel(boolean cancel) {
    this.cancel = cancel;
  }

  /** @return the clear */
  public boolean isClear() {
    return clear;
  }

  /** @param clear the clear to set */
  public void setClear(boolean clear) {
    this.clear = clear;
  }

  /** @return the save */
  public boolean isSave() {
    return save;
  }

  /** @param save the save to set */
  public void setSave(boolean save) {
    this.save = save;
  }
}
